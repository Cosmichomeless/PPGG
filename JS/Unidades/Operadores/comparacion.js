let a = 10;
console.log(a < 10, "menor que"); // esto es menor que
console.log(a > 10, "mayor que"); // esto es mayor que

console.log(a == 10); // esto es igual que 10
console.log(a != 10); // esto es diferente que 10
console.log(a === 10); // esto es estrictamente igual que 10
console.log(a === "10"); // esto es estrictamente igual que "10"
console.log(a !== "10"); // esto es estrictamente igual que "10"
