let a = 5;
let b = 2;

console.log(a + b, "suma"); // esto es una suma
console.log(a - b, "resta"); // esto es una resta
console.log(a * b, "multiplicacion"); // esto es una multiplicacion
console.log(a / b, "division"); // esto es una division
console.log(a % b, "modulo"); // esto es un modulo
console.log(a ** b, "exponenciacion"); // esto es una exponenciacion
console.log(a++, "incremento"); // esto es un incremento
console.log(a--, "decremento"); // esto es un decremento
